def cut(old, new='', label=''):
    """Replace old with new. Assert exactly 1 occurrence."""
    global text, count
    n = text.count(old)
    assert n == 1, f"[{label}] expected 1, got {n}"
    text = text.replace(old, new)
    count += 1
    print(f"  [{count}] {label}")

def cut_first(old, new='', label=''):
    """Replace first occurrence only (for duplicates)."""
    global text, count
    n = text.count(old)
    assert n >= 1, f"[{label}] expected >=1, got {n}"
    text = text.replace(old, new, 1)
    count += 1
    print(f"  [{count}] {label} (first of {n})")

