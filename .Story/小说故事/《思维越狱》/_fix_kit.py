def main():
    import glob
    fixed = 0
    for num, old, new in FIXES:
        files = glob.glob(os.path.join(BASE, f'ASTO.S{num:03d}.*.md'))
        if not files:
            print(f'S{num:03d}: 文件未找到')
            continue
        filepath = files[0]
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()

        if old not in content:
            print(f'S{num:03d}: 旧文本未匹配，跳过')
            # debug: show what's actually there
            idx = content.find(f'4.  **')
            if idx > 0:
                snippet = content[idx:idx+120]
                print(f'  实际内容: {repr(snippet[:100])}')
            continue

        content = content.replace(old, new, 1)
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
        fixed += 1
        print(f'S{num:03d}: 已修复')

    print(f'\n完成，共修复 {fixed}/{len(FIXES)} 处。')


if __name__ == '__main__':
    main()
